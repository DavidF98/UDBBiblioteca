        // TODO add your handling code here:
        //boton restablecer contrase√±a
        String usuario = jTextField1.getText();
        String contra = jPasswordField1.getText();
        String contra_confirm = jPasswordField2.getText();
        
        String url = "UPDATE usuario SET contra='"+contra_confirm+"' WHERE usuario='"+usuario+"'";
        
        try{
            
            PreparedStatement ps = con.prepareStatement(url);
            if(contra_confirm.equals(contra)){    
                ps.execute();
                JOptionPane.showMessageDialog(null, "Contrase√±a actualizada!");
            }else{
                JOptionPane.showMessageDialog(null, "Contrase√±a no es similar!");
            }
            
          
            
            
            
        }catch(SQLException ex){
        
        }
        
