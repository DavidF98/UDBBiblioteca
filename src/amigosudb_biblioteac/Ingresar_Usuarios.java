    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField1.getText();
        String apellido= jTextField2.getText();
        String usuario = jTextField3.getText();
        String contra = jPasswordField1.getText();
        String tipo = jList1.getSelectedValue();
        String correo= jTextField4.getText();
        
        String url = "INSERT INTO usuario (contra, nombre, apellido, tipo, usuario, correo) VALUES ('"+contra+"','"+nombre+"','"+apellido+"','"+tipo+"','"+usuario+"','"+correo+"')";
        
        try{
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select usuario,tipo from usuario where usuario='"+usuario+"'AND tipo='"+tipo+"'");
            if ( !rs.next() ){
               
                    PreparedStatement ps = con.prepareStatement(url);
                    ps.execute();
                    JOptionPane.showMessageDialog(null, "Registro exitoso!");
                
                //insert id en cliente.codecliente
                
            } else{
                JOptionPane.showMessageDialog(null, "Usuario ya registrado");

            }     
            
        }catch (SQLException ex){
            
            System.out.println(ex.toString());
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

