    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String user = jTextField1.getText();
        String pass = jPasswordField1.getText();

        String url = "select usuario, contra,tipo from usuario where usuario='" + user + "'";
        
        try {
            
            PreparedStatement ps = con.prepareStatement(url);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {

                String u = rs.getString("usuario");
                String p = rs.getString("contra");
                String priv = rs.getString("tipo");

                if (pass.equals(p)) {
                    if (priv.equals("Administrador")) {
                        Administrador ventanaAdmin = new Administrador();
                        ventanaAdmin.setVisible(true);
                        this.setVisible(false);
                    } else if (priv.equals("Alumno")) {
                        Alumnos ventanaAlumnos = new Alumnos();
                        ventanaAlumnos.setVisible(true);
                        this.setVisible(false);
                    }else{
                        Profesor ventanaProfe = new Profesor();
                        ventanaProfe.setVisible(true);
                        this.setVisible(false);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "CONTRASENA INCORRECTA");
                }

            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO EXISTE");
            }

        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

